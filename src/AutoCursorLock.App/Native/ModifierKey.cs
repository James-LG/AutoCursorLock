        /// No modifier key.
        /// </summary>
        None,

        /// <summary>
        /// The "Alt" key.
        /// </summary>
        Alt,

        /// <summary>
        /// The "Ctrl" key.
        /// </summary>
        Control,

        /// <summary>
        /// The "Shift" key.
        /// </summary>
        Shift,

        /// <summary>
        /// The "Windows" key.
        /// </summary>
        Windows,
    }
}
